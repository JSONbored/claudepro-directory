# Function to run command with timeout (cross-platform, robust for macOS)
# Returns exit code 124 if timeout occurred
# Usage: run_with_timeout <seconds> <command> [args...]
run_with_timeout() {
  local timeout_seconds="$1"
  shift
  local cmd_args=("$@")  # Store all remaining arguments as array
  
  # Create temporary files for output
  local output_file=$(mktemp 2>/dev/null || echo "/tmp/supabase_timeout_$$_$RANDOM")
  local error_file=$(mktemp 2>/dev/null || echo "/tmp/supabase_error_$$_$RANDOM")
  
  # Cleanup function
  local cleanup_done=false
  cleanup_timeout() {
    if [ "$cleanup_done" = false ]; then
      rm -f "$output_file" "$error_file" 2>/dev/null
      cleanup_done=true
    fi
  }
  
  # Run command in background, capturing both stdout and stderr
  # Use array expansion to properly handle arguments with spaces
  "${cmd_args[@]}" > "$output_file" 2> "$error_file" &
  local cmd_pid=$!
  
  # Monitor process with timeout
  # Check every 0.5 seconds, so we need 2x iterations for timeout_seconds
  local max_iterations=$((timeout_seconds * 2))
  local iteration=0
  local process_done=false
  local exit_code=0
  
  while [ $iteration -lt $max_iterations ] && [ "$process_done" = false ]; do
    # Check if process is still running
    if ! kill -0 $cmd_pid 2>/dev/null; then
      # Process finished - wait to get exit code
      wait $cmd_pid 2>/dev/null
      exit_code=$?
      process_done=true
    else
      sleep 0.5
      iteration=$((iteration + 1))
    fi
  done
  
  # If process is still running after timeout, kill it
  if [ "$process_done" = false ]; then
    # Try graceful termination first
    kill -TERM $cmd_pid 2>/dev/null
    sleep 0.5
    
    # Force kill if still running
    if kill -0 $cmd_pid 2>/dev/null; then
      kill -KILL $cmd_pid 2>/dev/null
      sleep 0.5
    fi
    
    # Kill any child processes
    pkill -P $cmd_pid 2>/dev/null
    
    # Output error message
    echo "Command timed out after ${timeout_seconds}s (process killed)" > "$error_file"
    exit_code=124
  fi
  
  # Output the result (combine stdout and stderr)
  cat "$output_file" 2>/dev/null
  cat "$error_file" 2>/dev/null
  
  cleanup_timeout
  return $exit_code
}

# Function to get current IP address (fallback if USER_IP not set)
get_current_ip() {
  # Use provided IP if available, otherwise try to detect
  if [ -n "$USER_IP" ]; then
    echo "$USER_IP"
    return 0
  fi
  
  # Try IPv6 first, then IPv4
  IP=$(curl -s --max-time 5 https://api64.ipify.org 2>/dev/null || curl -s --max-time 5 https://api.ipify.org 2>/dev/null || echo "")
  if [ -z "$IP" ]; then
    echo "Unable to detect IP address"
    return 1
  fi
  echo "$IP"
}

# Function to unban IP
unban_ip() {
  local ip="$1"
  if [ -z "$ip" ]; then
    echo "⚠️  No IP provided for unban"
    return 1
  fi
  
  echo ""
  echo "   🔓 Unbanning IP: $ip..."
  if supabase network-bans remove --experimental --project-ref "$FEATURE_BRANCH_REF" --db-unban-ip "$ip" 2>&1; then
    echo "   ✅ IP unbanned successfully"
    return 0
  else
    echo "   ⚠️  Failed to unban IP (may not be banned or already unbanned)"
    return 1
  fi
}

